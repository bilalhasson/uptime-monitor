class TeamModelTests(TestCase):
    def setUp(self):
        self.alice = User.objects.create_user("alice", "alice@test.com", "pass1234")
        self.bob = User.objects.create_user("bob", "bob@test.com", "pass1234")
        self.team = Team.objects.create(name="Platform", created_by=self.alice)
        TeamMembership.objects.create(
            team=self.team, user=self.alice, role=TeamMembership.Role.ADMIN
        )

    def test_str(self):
        self.assertEqual(str(self.team), "Platform")

    def test_membership_role_default_is_member(self):
        m = TeamMembership.objects.create(team=self.team, user=self.bob)
        self.assertEqual(m.role, TeamMembership.Role.MEMBER)

    def test_membership_unique_per_user(self):
        from django.db import IntegrityError

