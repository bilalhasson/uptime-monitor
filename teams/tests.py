class LastAdminGuardTests(TestCase):
    def setUp(self):
        self.client = Client()
        self.alice = User.objects.create_user("alice", "alice@test.com", "pass1234")
        self.bob = User.objects.create_user("bob", "bob@test.com", "pass1234")
        self.team = Team.objects.create(name="Platform", created_by=self.alice)
        self.alice_m = TeamMembership.objects.create(
            team=self.team, user=self.alice, role=TeamMembership.Role.ADMIN
        )
        self.bob_m = TeamMembership.objects.create(team=self.team, user=self.bob)
        self.client.login(username="alice", password="pass1234")

    def test_cannot_remove_last_admin(self):
        response = self.client.post(
            f"/settings/teams/{self.team.pk}/members/{self.alice.pk}/remove/"
        )
        self.assertRedirects(response, f"/settings/teams/{self.team.pk}/")
        self.assertTrue(
            TeamMembership.objects.filter(pk=self.alice_m.pk).exists()
        )

    def test_cannot_demote_last_admin(self):
        self.client.post(
            f"/settings/teams/{self.team.pk}/members/{self.alice.pk}/role/",
            {"role": "member"},
        )
        self.alice_m.refresh_from_db()
        self.assertEqual(self.alice_m.role, TeamMembership.Role.ADMIN)

    def test_can_remove_member(self):
        self.client.post(
            f"/settings/teams/{self.team.pk}/members/{self.bob.pk}/remove/"
        )
        self.assertFalse(TeamMembership.objects.filter(pk=self.bob_m.pk).exists())

    def test_can_demote_after_promoting_another(self):
        # Promote bob, then alice can be demoted.
        self.client.post(
            f"/settings/teams/{self.team.pk}/members/{self.bob.pk}/role/",
            {"role": "admin"},
        )
        self.client.post(
            f"/settings/teams/{self.team.pk}/members/{self.alice.pk}/role/",
            {"role": "member"},
        )
        self.alice_m.refresh_from_db()
        self.assertEqual(self.alice_m.role, TeamMembership.Role.MEMBER)


