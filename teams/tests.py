class InviteFlowTests(TestCase):
    def setUp(self):
        self.client = Client()
        self.alice = User.objects.create_user("alice", "alice@test.com", "pass1234")
        self.bob = User.objects.create_user("bob", "bob@test.com", "pass1234")
        self.team = Team.objects.create(name="Platform", created_by=self.alice)
        TeamMembership.objects.create(
            team=self.team, user=self.alice, role=TeamMembership.Role.ADMIN
        )

    @patch("teams.views.resend.Emails.send")
    def test_admin_invite_creates_invite_and_sends_email(self, mock_send):
        with self.settings(RESEND_API_KEY="test-key"):
            self.client.login(username="alice", password="pass1234")
            self.client.post(
                f"/settings/teams/{self.team.pk}/invite/",
                {"email": "bob@test.com", "role": "member"},
            )
        invite = TeamInvite.objects.get(email="bob@test.com")
        self.assertEqual(invite.team, self.team)
        mock_send.assert_called_once()
        payload = mock_send.call_args.args[0]
        self.assertEqual(payload["to"], ["bob@test.com"])
        self.assertIn(invite.token, payload["text"])

    @patch("teams.views.resend.Emails.send")
    def test_invite_skipped_when_no_api_key(self, mock_send):
        with self.settings(RESEND_API_KEY=""):
            self.client.login(username="alice", password="pass1234")
            self.client.post(
                f"/settings/teams/{self.team.pk}/invite/",
                {"email": "bob@test.com", "role": "member"},
            )
        # Invite row still created, but no email attempted.
        self.assertTrue(TeamInvite.objects.filter(email="bob@test.com").exists())
        mock_send.assert_not_called()

    def test_accept_valid_invite_adds_membership(self):
        invite = TeamInvite.objects.create(
            team=self.team, email="bob@test.com", role=TeamMembership.Role.MEMBER
        )
        self.client.login(username="bob", password="pass1234")
        response = self.client.get(f"/teams/invite/{invite.token}/")
        self.assertRedirects(response, f"/settings/teams/{self.team.pk}/")
        self.assertTrue(
            TeamMembership.objects.filter(team=self.team, user=self.bob).exists()
        )
        invite.refresh_from_db()
        self.assertIsNotNone(invite.accepted_at)

    def test_accept_expired_invite_rejected(self):
        invite = TeamInvite.objects.create(
            team=self.team,
            email="bob@test.com",
            expires_at=timezone.now() - timedelta(days=1),
        )
        self.client.login(username="bob", password="pass1234")
        response = self.client.get(f"/teams/invite/{invite.token}/")
        self.assertRedirects(response, "/settings/teams/")
        self.assertFalse(
            TeamMembership.objects.filter(team=self.team, user=self.bob).exists()
        )

    def test_accept_used_invite_rejected(self):
        invite = TeamInvite.objects.create(
            team=self.team, email="bob@test.com", accepted_at=timezone.now()
        )
        self.client.login(username="bob", password="pass1234")
        self.client.get(f"/teams/invite/{invite.token}/")
        self.assertFalse(
            TeamMembership.objects.filter(team=self.team, user=self.bob).exists()
        )

    def test_accept_requires_login(self):
        invite = TeamInvite.objects.create(team=self.team, email="bob@test.com")
        response = self.client.get(f"/teams/invite/{invite.token}/")
        self.assertEqual(response.status_code, 302)
        self.assertIn("/accounts/login/", response.url)


class ActiveTeamResolutionTests(TestCase):
    """get_active_team falls back to Personal when the reference is stale."""

    def setUp(self):
        self.factory_client = Client()
        self.alice = User.objects.create_user("alice", "alice@test.com", "pass1234")
        self.team = Team.objects.create(name="Platform", created_by=self.alice)
        TeamMembership.objects.create(
            team=self.team, user=self.alice, role=TeamMembership.Role.ADMIN
        )

    def _request_with_session(self, user, session_data):
        from django.contrib.sessions.backends.db import SessionStore
        from django.test import RequestFactory

        request = RequestFactory().get("/")
        request.user = user
        request.session = SessionStore()
        for k, v in session_data.items():
            request.session[k] = v
        return request

    def test_returns_team_when_member(self):
        request = self._request_with_session(self.alice, {"active_team_id": self.team.pk})
        self.assertEqual(get_active_team(request), self.team)

    def test_returns_none_when_not_member(self):
        bob = User.objects.create_user("bob", "bob@test.com", "pass1234")
        request = self._request_with_session(bob, {"active_team_id": self.team.pk})
        self.assertIsNone(get_active_team(request))

    def test_returns_none_when_unset(self):
        request = self._request_with_session(self.alice, {})
        self.assertIsNone(get_active_team(request))
